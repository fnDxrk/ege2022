#Нетривиальный делитель натурального числа — это делитель, который не равен 1 и
#самому числу. Среди натуральных чисел из интервала [13245; 15289] найти такие,
#которые имеют ровно двадцать нетривиальных делителей. 
#В ответе необходимо вывести само число и его максимальный делитель. 
#Найденные числа вывести на экран в порядке возрастания.
for i in range(13245, 15289 + 1):
    d = []
    for j in range(2, int(i**0.5) + 1):
        if j * j == i:
        	d.append(j)
        elif i % j == 0:
            d.append(j)
            d.append(i//j)
    if len(d) == 20:
        print(i, max(d))
# ПОЯСНЕНИЕ: j ищем до корня i, чтобы программа быстрее работала
# и не искала следующие цифры, тк они идут попарно
# Пример: 20 - 1 2 4 5 10 20
# 1*20; 2*10; 4*5

