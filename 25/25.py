# Ваша задача: определить первые 5 чисел, которые больше 800000 и для которых
#разность максимального и минимального натуральных делителей, не считая единицы и
#самого числа, кратна 16. Программа должна найти и вывести первые 5 таких чисел и
#соответствующие им значения разности максимального и минимального делителей. 
#Формат вывода: для каждого из 5 таких найденных чисел в отдельной строке сначала
#выводится само число, затем через пробел — значение разности.
n = 80001 # Число больше 80000
c = 0
while c != 5: # Выполняется цикл, пока не найдём 5 чисел
    f = 0
    for i in range(2, n):
        if n % i == 0:
            f = n//i - i #Разность макс и мин делителей
            break
    if f % 16 == 0: # Если разность делится на 16
        print(n, f) # Вывод числа и подходящей разности
        c += 1
    n += 1 # Увеличиваем первоначальное число


